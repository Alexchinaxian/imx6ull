#ifndef MAIN_H
#define MAIN_H

#include <cstdint>

// 前置声明
class ServiceManager;
class SystemBeep;

// ==========================================
// 全局常量
// ==========================================

/**
 * @brief 软件版本号（主版本.次版本.修订版本）
 * 格式: {主版本, 次版本, 修订版本}
 * 例如: {1, 0, 2} 表示 V1.0.2
 */
extern const uint8_t SoftwareVersion[3];

// ==========================================
// 全局变量
// ==========================================

/**
 * @brief 全局服务管理器实例指针
 * 
 * 功能说明:
 *   负责管理所有系统服务（硬件初始化、温度监控、Modbus通信等）
 *   在 main() 函数中初始化
 *   在信号处理器中用于优雅关闭所有服务
 */
extern ServiceManager *g_serviceManager;

/**
 * @brief 全局系统蜂鸣器实例指针
 * 
 * 功能说明:
 *   负责系统提示音的播放（启动、关机、告警等）
 *   在 main() 函数中初始化
 *   在信号处理器中用于播放关机提示音
 */
extern SystemBeep *g_systemBeep;

#endif // MAIN_H

